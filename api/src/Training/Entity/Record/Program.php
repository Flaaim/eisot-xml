    private function __construct(
        private int $id,
        private string $title,
    ) {}

    public function __toString(): string
    {
        return $this->title;
    }

    /**
     * Основной фабричный метод: создать программу по числовому ID из каталога Минтруда.
     */
    public static function fromId(int $id): self
    {
        if (!isset(self::CATALOG[$id])) {
            throw new DomainException(\sprintf('Unknown training program ID: %d.', $id));
        }

        return new self($id, self::CATALOG[$id]);
    }

    /**
     * Обратная совместимость: создать программу по названию (title).
     *
     * Используется в ProgramType::convertToPHPValue при чтении старых строковых данных из БД.
     */
    public static function fromString(string $title): self
    {
        $id = array_search($title, self::CATALOG, true);

        if (false === $id) {
            throw new DomainException(\sprintf('Unknown training program: "%s".', $title));
        }

        return new self($id, $title);
    }

    public function getId(): int
    {
        return $this->id;
    }

    public function getTitle(): string
    {
        return $this->title;
    }

    /**
     * @deprecated Используйте getTitle(). Оставлен для обратной совместимости.
     * @psalm-suppress PossiblyUnusedMethod
     */
    public function getValue(): string
    {
        return $this->title;
    }

    /**
     * Возвращает полный каталог допустимых программ.
     *
     * @return array<int, string>
     */
    public static function catalog(): array
    {
        return self::CATALOG;
    }

    /**
     * Возвращает список допустимых ID программ.
     *
     * @return int[]
     */
    public static function allowedIds(): array
    {
        return array_keys(self::CATALOG);
    }

    public function isEqualTo(self $other): bool
    {
        return $this->id === $other->id;
    }
